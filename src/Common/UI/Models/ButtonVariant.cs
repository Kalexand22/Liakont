namespace Stratum.Common.UI.Models;

/// <summary>Visual style variant for buttons.</summary>
public enum ButtonVariant
{
    /// <summary>Primary action — blue.</summary>
    Primary,

    /// <summary>Secondary action — grey/outline.</summary>
    Secondary,

    /// <summary>Dangerous/destructive action — red.</summary>
    Danger,

    /// <summary>Positive/confirmation action — green.</summary>
    Success,

    /// <summary>Warning/caution action — amber.</summary>
    Warning,
}
